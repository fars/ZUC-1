void TestLittleEndian()
{
	u32 n = 0;
	u8 i = 0;
	unsigned char pData[4] = {0};

	/*First display*/
	n = 1;
	memcpy(pData, &n, 4);
	
	printf("Data in memory:\t0x");
	for (i=0; i<4; i++)
	{
		printf("%x", pData[i]);
	}
	printf("\n");
	printf("Data in CPU:\t0x%x\n", n);

	/*Second display*/
	n = (n << 1);
	memcpy(pData, &n, 4);
	
	printf("Data in memory:\t0x");
	for (i=0; i<4; i++)
	{
		printf("%x", pData[i]);
	}
	printf("\n");
	printf("Data in CPU:\t0x%x\n", n);
}

